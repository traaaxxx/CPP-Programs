//program to find the size of fundamental data types

#include <iostream>
using namespace std;
 
int main() 
{
   cout << "The Size of Fundamental Data Types:\n\n";
   cout << "The sizeof(char) is:    " << sizeof(char) << " byte(s) \n" ;
   cout << "The sizeof(int) is:     " << sizeof(int) << " byte(s) \n" ;
   cout << "The sizeof(float) is:   " << sizeof(float) << " byte(s) \n" ;
   cout << "The sizeof(double) is:  " << sizeof(double) << " byte(s) \n";
   cout << "The sizeof(bool) is :   " << sizeof(bool) << " byte(s) \n";
   return 0;
}